# Operator Perbandingan
# hasil dari operator perbandingan akan bernilai boolean

# > , lebih dari
print(1>4)

# < , kurang dari
print(3<6)

# >= , Lebih dari sama dengan
print(5>=5)

# <= , kurang dari sama dengan
print(5<=4)

# == , sama dengan
print(7==7)

# != , tidak sama dengan
print(6 != 6)
